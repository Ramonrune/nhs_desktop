    public AddPatientTagWindow() {
        setResizable(false);

        initComponents();
        
        setLocationRelativeTo(null);
        
        NFCReaderAddTag.AddPatientTagWindow = AddPatientTagWindow.this;

        if (!NFCReaderAddTag.setupOk()) {
            ExecutorService executor = Executors.newSingleThreadExecutor();
            executor.execute(() -> {

                while (cont) {

                    ExecutorService executor11 = Executors.newSingleThreadExecutor();
                    executor11.execute(() -> {
                        if (NFCReaderAddTag.setupOk()) {

                            statusOk();
                            executor11.shutdown();

                        } else {
                            statusNotOk();

                        }
                    });

                    try {

                        TimeUnit.MILLISECONDS.sleep(1000);

                    } catch (InterruptedException ex) {
                        Logger.getLogger(AddPatientTagWindow.class.getName()).log(Level.SEVERE, null, ex);
                    }

                }

            });
        } else {
            statusOk();
        }
        
         addWindowListener(new WindowAdapter() {
            @Override
            public void windowClosed(WindowEvent e) {
                System.out.println("fechando");
                NFCReaderAddTag.close();
            }
        });
    }
